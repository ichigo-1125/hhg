//------------------------------------------------------------------------------
//  count
//
//  マクロに与えられた引数の数をカウントする。
//------------------------------------------------------------------------------
#[doc(hidden)]
#[macro_export]
macro_rules! count
{
    ( @subst $( $x: tt )* ) => (());
    ( $( $rest: expr ),* ) => (<[()]>::len(&[$($crate::count!(@subst $rest)),*]));
}
